<div class="about">
	<h2>About Us</h2>

	<p>The Ely Watercolor Club was founded in 1985 by local watercolor artists in Ely Minnesota.</p>

	<p>
		We are a group of local artists, and we get together to learn, practice painting and support each other's artistic endeavors. 
	</p>
	
	<p>
		Every year we have a huge show & sale in Ely Minnesota. We had to cancel the show in 2020, and again in 2021 due to Covid-19. This year we are moving forward with the show, and it promises to be bigger than ever!
	</p>
	<p>
		Many of us also participate in other local artist groups and our art can be found around town in
		many businesses and at the <a href="https://elysartandsoul.com/" target="_blank"
			>Art & Soul Gallery</a
		>.
	</p>
	
	<p class="session-info">
		Currently we are meeting every Thursday from 9:30am - 12:30pm at the <a href="https://elyfolkschool.org/">The Ely Folk School</a> to paint. It is open studio time, and you can come by with your supplies (even if it isn't watercolor) and make art. Admission is free.
	</p>
</div>

